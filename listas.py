lista = [1,2,3,4,5]
#agregar algo a la lista al final
lista.append(6)
print(lista)

#instertar algo a la lista en la posición que indicas, también indicas el objeto a insertar
lista.insert(6,"nicolás")
print(lista)

#eliminar un objeto de la lista, debes escribir el objeto a eliminar
lista.remove("nicolás")
print(lista)

#invertir una lista
lista.reverse()
print(lista)

#para ordenar de mayor a menor
lista.sort()
print(lista)